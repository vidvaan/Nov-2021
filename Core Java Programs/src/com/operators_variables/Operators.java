package com.operators_variables;

public class Operators {
	public static void main(String[] args) {
		
		int a=10;
		int b=23;
		System.out.println("a==10, b=23 ");
		System.out.println(" ");
		//Arithmetic operators.(+,-,*,/,%)
		
		System.out.println("Arithmetic operators.");
		System.out.println("Add : " +(a+b));
		System.out.println("Sub : " +(a-b));
		System.out.println("Mul : " +(a*b));
		System.out.println("Div : " +(a/b));
		System.out.println("Mod : " +(a%b));
		System.out.println("  ================================== ");
		
		//Relational operators.(==,<,>,<=,>=,!=)
		
		System.out.println("Relational operators.");
		System.out.println("== : "+(a==b));
		System.out.println("<  : "+(a<b));
		System.out.println(">  : "+(a>b));
		System.out.println("<= : "+(a<=b));
		System.out.println(">= : "+(a>=b));
		System.out.println("!= : "+(a!=b));
		System.out.println("  ================================== ");
		
		//Assignment operators.(+=((b+=a)=(b=b+a)),-=,*=,/=,%=)
		
		System.out.println("Assignment operators.");
		System.out.println("+=  : "+(a+=b));
		System.out.println("-=  : "+(a-=b));
		System.out.println("*= : "+(a*=b));
		System.out.println("/=  : "+(a/=b));
		System.out.println("%=  : "+(a%=b));
		System.out.println("  ================================== ");
		
		//Post and PreIncrement, post and pre Decrement operators.(++i,--i,i++,i--)
		
		System.out.println("Post and PreIncrement, post and pre Decrement operators.");
		
		
		
		
		
		
	
	}   

}
